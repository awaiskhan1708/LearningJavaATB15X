package ex_04_Operators;

public class Lab032_OperatorsOne {
    public static void main(String[] args) {
        //Arithmatic operators
        // + - * / %
        int a = 10;
        int b = 2;
        System.out.println(a + b); //a and b are operands + is operator
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        //Binary operator  because one operator
 float c = 3.0f;
        System.out.println(a/c);
    }
}
