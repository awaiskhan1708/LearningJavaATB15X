package ex_04_Operators;

public class Lab034_RelationalOperator {
    public static void main(String[] args) {
        // < Less Than
        // <= Less than equal to
        // > Greater Than
        // >+ Greater than equal to
   // == Equal to
   // != Not equal to
        int a = 10;
        int b = 20;
        boolean c = a > b;
        System.out.println(c);

        int age_awais = 27;
        int age_wasim = 29;
        boolean age_is_lessthan =  age_awais >= age_wasim;
        System.out.println(age_is_lessthan);
    }
}
